	<h2>Payment methods rules</h2>
	<div class="metadata">Last updated:<br>June 17, 2023</div>
	<h4 class="mt-40">Overview</h4>
    <p>At GRO Payments LLC, we provide a variety of payment method options to facilitate transactions for our clients and their customers. This document outlines the rules and guidelines associated with the use of different payment methods on our platform.</p>

    <h4 class="mt-40">Acceptance of Payment Methods</h4>
    <p>GRO Payments LLC allows merchants to accept various payment methods, including credit and debit cards, e-wallets, and other alternative payment solutions, subject to our approval and adherence to the applicable rules set forth by the payment methods' governing bodies.</p>

    <h4 class="mt-40">Compliance with Payment Network Rules</h4>
    <p>Merchants using GRO Payments LLC's services must comply with all rules and regulations imposed by the payment networks and processors. This includes, but is not limited to, rules regarding transaction processing, data security, and chargeback procedures.</p>

    <h4 class="mt-40">Transaction Security</h4>
    <p>All transactions processed through GRO Payments LLC must meet stringent security standards to protect sensitive payment information. Merchants are required to maintain PCI DSS compliance and implement fraud prevention measures.</p>

    <h4 class="mt-40">Prohibited Use of Payment Methods</h4>
    <p>Payment methods may not be used for unauthorized transactions, illegal activities, or fraudulent purposes. GRO Payments LLC reserves the right to suspend or terminate services for any merchant found to be in violation of these terms.</p>

    <h4 class="mt-40">Chargeback and Dispute Management</h4>
    <p>Merchants must manage chargebacks and disputes according to the guidelines provided by GRO Payments LLC and the respective payment method providers. Failure to adhere to these guidelines may result in additional fees or service restrictions.</p>

    <h4 class="mt-40">Updates to Payment Method Rules</h4>
    <p>GRO Payments LLC may update these payment method rules from time to time to reflect changes in the regulatory landscape or in the features of the payment methods offered. Merchants are expected to review and comply with the updated rules as published.</p>

